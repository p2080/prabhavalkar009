package Loop;

public class For_loop_2 {

	public static void main(String[] args) {
		//for(declare,initialize,increment/decrement)
		
		for (int b=0; b<=50;b=b+5)  //declare,initialize,increment/decrement
		{
			
			System.out.println(b);
		}
		
		System.out.println("=======");
			
		for(int a=5;a<=50;a=a+5)
		{
			
			
			System.out.println(a);
		}

	}

}
