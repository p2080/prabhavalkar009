package Array;

public class multidimensionalArray2 {

	public static void main(String[] args) 
	{
    String name[][] = {{"velocity","corporate","tranning"},{"center","katarj","pune"}};
    
    for(int i=0;i<=1;i++)
    {
    	for(int j=0;j<=2;j++)
    	{ 
    		System.out.print(name[i][j]+" ");
    	}
    	System.out.println();
    }
		
		
		
		
	}

}
